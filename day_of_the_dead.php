              <strong>Tipping in Mexico:</strong> Tipping guidelines for Mexico are nearly the same as tipping guidelines used in the United States or Canada, with some exceptions. Most service employees earn very little or no base salary and the tips they earn comprise the vast majority of their overall income. In restaurants, it is customary to leave your server a tip of 15% of the cost of the food/beverages before the value added tax. <a href="http://www.tripadvisor.com/Travel-g150768-s606/Mexico:Tipping.And.Etiquette.html" class="link-select" target="_blank">Click here</a> to read a brief article about tipping in Mexico.


