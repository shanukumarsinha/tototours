              <h3>Money Matters</h3>
              The currency of Bolivia is the Boliviano (BOB, symbol Bs). Notes are in denominations of Bs200, 100, 50, 20, 10 and 5. Coins are in denominations of Bs5, 2 and 1, and 50, 20 and 10 centavos. The Boliviano is tied to the US Dollar, and on September 3, 2016 they were valued at BOB 6.73 to US $1. Money can be changed in hotels and casas de cambio. American Express, Diners Club, MasterCard and Visa are accepted in most mid- to top-range hotels and restaurants, but otherwise have limited acceptance. There are ATMs in most larger towns and cities, but you will need to carry cash for our trips into the countryside. Click here to find the most up-to-date exchange rate.<br><br>
