              <h3>What to Bring</h3>
              Bolivia has a mild climate, but temperatures can differ dramatically between day and night. The northeast slopes of the Andes Mountains are semi-tropical. The mountain areas can become very cold at night. Visitors sometimes find La Paz uncomfortable because of the thin air at its high altitude. Be sure to check with your doctor and follow all recommendations for prevention of altitude sickness. For clothes, think about packing things you can layer! Be sure to bring two pieces of luggage—one a small overnight bag for our short excursions lasting just one or two nights. Warmer clothing is necessary at night in the Altiplano, especially La Paz and Potosi. Pack any medications you need in a carry-on bag. Leave valuable jewelry at home! Laundry services will be available at our hotels, so you do not need to bring enough clothing for fresh outfit every day during the tour.
