              </ul>
              <h3>Money Matters</h3>
              Mexico’s currency is the Mexican Peso (MXP), with bills in 20, 50, 100 and 500 MXP denominations and coins in .50, 1, 2, 5 and 10 pesos. Although most tourist centers widely accept U.S. dollars, we recommend using Pesos during your visit. Money exchange facilities offer the best exchange rate. On April 18, 2017, the exchange rate was 18.83 MXP to the U.S. $1.00.<br/>
              <strong>Tipping in Mexico: </strong>Tipping guidelines for Mexico are nearly the same as tipping guidelines used in the United States or Canada, with some exceptions. Most service employees earn very little or no base salary and the tips they earn comprise the vast majority of their overall income. In restaurants, it is customary to leave your server a tip of 15% of the cost of the food/beverages before the value added tax.<br><br>
