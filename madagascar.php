                  <h5 class="mb-20">FORT DAUPHIN / BERENTY RESERVE</h5>
                  <p>This morning we fly to picturesque Fort Dauphin in the most exotic and famous part of Madagascar. From Fort Dauphin, board our minibus and drive west to Berenty, home of the Antandroy people. This region of “spiny desert” has weird cactus-like trees. Here, the Mahafaly tribe erects their intriguing and often entertaining “aloalo” (funerary pole sculpture) above the graves. The landscape is dramatic, with rugged green hills for the first half of the journey giving way to a flatlands and a skyline interspersed with baobab and spiny trees, including the “octopus tree” and more spiny plants.<br><br>
                  Bordering the Mandrare River, the Berenty Reserve includes more than 600 acres of gallery and riverine forest. For three days, we explore this superb private wildlife reserve, famous for its population of ring-tailed lemurs and sifakas (the most picturesque primate with creamy white fur and darker face).<br><br>
                  It is not unusual for the photogenic ring-tailed lemurs to take bananas from your hand. They are used to humans and are quite tame. You’ll wake at dawn to enjoy the lemurs’ antics and hear the early chorus of birdlife. We may also see hundreds of fruit bats clinging just under the forest canopy. There are 56 species of birds, as well as thousands of butterflies and different species of chameleons. Overnight at Berenty Private Reserve.      <strong style="font-style: italic">(All Meals)</strong></p>
