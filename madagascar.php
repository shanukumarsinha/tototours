                  <h5 class="mb-20">TULEAR / RANOHIRA / ISALO PARK</h5>
                  <p>Take a morning flight to Tulear, where we board vehicles for the drive to Isalo National Park. There is a dramatic change in scenery during the drive, becoming more desert-like, allowing the sky to fill with all the radiant colors of the setting sun. One of the most frequented parks in Madagascar, Isalo National Park is a spectacular site established in 1962 and covering 315 square miles. The rocks here (known as “ruiniformes”) date back to the Jurassic Period and have been sculpted by wind and water into weird and wonderful shapes: eyes, beaks, and faces.<br><br>
                  Awaken to the view of the sandstone canyon massif, markedly different from any place you’ve visited so far on the trip and impressive in its grandeur and unusual eroded shapes. Succulents and fire-resistant trees, such as the tapia, on which the endemic silkworms feed, are native here.<br><br>
                  In the early morning, the black kite will soar regally overhead as if it owns the desert sky. The endemic Benson’s rock thrush is found only in this area and will be heard singing in the morning from the prominent rock perches. Spend your days here exploring the park looking for ringtails, sifakas, and brown lemurs. At the Isalo Interpretation Centre, learn about the local indigenous tribes whose mysterious burial sites are found here. Lodgings at the boundary of Isalo National Park.      <strong style="font-style: italic">(All Meals)</strong></p>
