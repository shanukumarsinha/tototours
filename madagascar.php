                  Due to its considerably larger size, Mantadia’s wildlife is even more diverse. Indri, diademed sifakas, and black-and-white ruffed lemurs can be found here, although seeing these animals will require some effort as you follow them on steep and narrow trails. Our Farewell Dinner will be in the evening of our last night here. Three nights at Andasibe.      <strong style="font-style: italic">(All Meals)</strong></p>
