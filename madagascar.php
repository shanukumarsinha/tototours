              <h3>Money Matters</h3>
              The official currency of Madagascar is the Malagasy Ariary (MGA). Visitors can exchange US dollars, pounds sterling and euro on arrival at the airport, although limits on the amount are in place. Banks are found in the cities and main tourist areas, although many will only take US dollars or the euro. Travelers’ checks are impossible to cash—don’t use them. ATMs are the easiest way to get cash and are found in city centers, but cash is king in the countryside. Credit cards are accepted at major hotels and stores in cities, but are not yet widely used as a form of payment and a service charge will most likely be applied for credit card payments. As of July 1 the Ariari was worth about 2,975 per US dollar. <a href="https://www.oanda.com/currency/converter/" target="_blank" class="link-select"> Click here</a> to see the most up-to-date exchange rates.<br><br>
