              <strong>Tipping in the Philippines:</strong>Tipping is not obligatory in the Philippines as the country does not have a tradition of receiving tips, but in some localities it is becoming more frequent. In restaurants, you may give a tip if the service is good (about 10% of the bill) and no service fee has already been added to the bill. At fast food eateries, your spare change is appreciated. To hotel bellboys, maids and cleaning staff and to taxi drivers, give at least the equivalent of $1 U.S. (50 PHP) per day per person. We will offer a group tip to our guides and drivers, and more details will be provided before departure.


