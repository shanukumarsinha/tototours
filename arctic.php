                  <h5>Hershel Island & the Yukon Territory | Safe Haven in the Farthest Reaches</h5>
                  <p>During a long mapping expedition in 1826, Captain Franklin was the first European to lay eyes on this unique island at the northernmost point of the Yukon Territory. Named by Franklin, Herschel Island is a landmark in the West Arctic and has since served alternately as a whaling station, relay station and refuge for travellers. The island teems with wildlife that includes the migrating bowhead whale, walrus, moose, musk ox, Arctic fox and 94 species of birds. It is also one of the only places on earth where you may see a grizzly bear, black bear and polar bear, the last of which live along the ice edge in summer. Evidence of the island’s whaling culture and Thule Inuit predecessors remains near the shoreline, though it may not for much longer; the island is subject to extreme coastal erosion and scientists predict the shoreline will disappear under the waves within 50 years.   |   <strong style="font-style: italic">Meals: B L D</strong></p>
