                  <h5>Western Greenland | Where Amundsen Began</h5>
                  <p>In 1906, polar explorer Roald Amundsen became the first person to successfully cross the entire Northwest Passage. Start your voyage exactly where he did along the stunning West Coast of Greenland and north into Baffin Bay, which you explore for six days. Your enrichment and lecture program also begins, offering the first of many opportunities to delve deeper into the story of this dramatic and remote region.<br>
                  Based on ice, weather and sea conditions, your captain and expedition crew determine the day’s best sightseeing opportunities, which may include:<br><br>
                  <strong>Sisimiut:</strong> North of the Arctic Circle, Sisimiut is both the northernmost city in Greenland able to maintain a year-round, ice-free port, as well as the southernmost town with sufficient snow for dogsledding in winter and spring. Visit the local museum with its exhibits on Inuit culture and Greenlandic colonial history, and meet a dog musher and his dogsled team.<br><br>
                  <strong>Disko Bay & Ilulissat:</strong> Cruise into Disko Bay, a wide inlet off of Baffin Bay first explored by Erik the Red in 985, when he established the first Norse settlements in Western Greenland. Discover the Ilulissat Icefjord, a UNESCO World Heritage Site, at the sea mouth of one of the fastest and most active glaciers in the world, Sermeq Kujalleq. The scene is spectacular with giant icebergs, floating growlers and bergy bits (large chunks of glacial ice), and the sounds of the calving ice-stream. Take a walking tour of Ilulissat, including a visit to the local history museum, and meet with villagers to learn about life in this often-harsh Arctic region. Enjoy a huskie dogsled demonstration and learn about the centuries-old methods of leather tanning still in practice today.<br><br>
                  <strong>Uummannaq Fjords:</strong> Located north of Ilulissat, the Uummannaq Fjord System is an awe-inspiring geological wonder teeming with marine life. Visit Karrat ø to enjoy the stunning view of the iceberg-studded bay and also discover the remains of ancient huts, made of whale bones and sod by traditional Thule hunters (ancestors of the Inuit) some 500 to 1,000 years ago.<br><br>
                  <strong>Upernavik:</strong> Founded in 1772, this summer camp for nomadic Greenland Inuit was an ideal hunting ground for whales, seals and fish. Witness this for yourself when you spend time with local villagers who still maintain a traditional way of life, living off the fish and seal populations common to the region. Visit the shops, church and local museum, the last of which offers an excellent display of Greenlandic life.   |  <strong style="font-style: italic"> Meals: B L D</strong>

