                  <h5>Nunavut & the Canadian Arctic Archipelago | Heart of the Northwest Passage</h5>
                  <p>Cruise west across Baffin Bay and into the Canadian Arctic Archipelago of Nunavut, where you begin your journey to the heart and history of the Northwest Passage. The A&K Expedition Team continues to share captivating lectures to enhance your own discovery.<br>
                  As ‘Le Boreal’ winds its way through legendary channels and inlets, your crew and Expedition Team determines the best route based on ice, weather and sea conditions. Exciting excursions await and are likely to include:<br><br>
                  Pond Inlet: Located on the Northern end of Baffin Island, Pond Inlet, which translates to “Place of Mittima’s Grave,” is the noted gateway to the fabled Northwest Passage and a rich archaeological site.<br><br>
                  After clearing customs formalities for Nunavut, set off for a shore excursion to an area originally inhabited by the ancient Thule. Visit the Nattinnak Visitor’s Center or Toonoonik Sahoonik Co-op, where you can shop for artisan carvings made from local red and green soapstone, beautiful wall hangings and other handcrafted goods.<br><br>
                  <strong>Lancaster Sound:</strong> Situated between Devon Island and Baffin Island, this body of water forms the eastern entrance to the Parry Channel and the Northwest Passage. It’s also home to a rich abundance of Arctic cod, which in turn draws copious populations of sea birds and marine mammals. Beluga and endangered bowhead whales, the narwhal with its spiraling tusk, ringed and bearded seals, the enchanting polar bear, and mustached walrus, as well as northern fulmars, black guillemots and Arctic terns — all are among the fantastic wildlife that inhabit the area. Some may even come into view on thrilling Zodiac excursions and landings.<br><br>
                  <strong>Beechey Island:</strong> Historic moments in Arctic exploration define this island, best known for providing a safe haven to British explorer Sir John Franklin in 1845. Look east toward Resolute Bay at the huge silhouette of Cape Riley and imagine what Captain Franklin saw here in Erebus Harbour, were he took shelter for two years before his ill-fated attempt to conquer the Northwest Passage. See the wooden grave markers for three of Franklin’s men, now bleached by the sun, and visit the cenotaph memorial erected in memory of the lost explorer. It is an unforgettable experience.<br><br>
                  <strong>Fort Ross:</strong> An abandoned trading post on the tip of Somerset Island, Fort Ross was founded in 1937 as a place for trappers to barter Arctic fox pelts in exchange for food and necessities. Explore the remaining wooden buildings of the post, which closed in 1948, and imagine life in such a barren landscape. Keep an eye out for bowhead whales or, perhaps, take a Zodiac excursion to CoBay, where polar bears have been known to feed.<br><br>
                  <strong>Gjoa Haven:</strong> During his first attempt to transit the Northwest Passage on ‘Gjøa,’ Roald Amundsen used this natural harbor as a respite while waiting for ice conditions to improve. For two years, he lived with the Netsilik Inuits, learning their skills for survival and more efficient travel, which would later prove invaluable in his successful South Pole expedition. Today, Gjoa Haven has a population of 1,200 and still bears the historic significance of playing a key role in polar exploration.<br><br>
                  <strong>Victoria Island:</strong> Cruise along the south coast of Victoria Island, which straddles both Nunavut and the Northwest Territories, voyaging through Queen Maud Gulf, Dease Strait and Coronation Gulf. Expedition stops may include bird sanctuary Jenny Lind Island, where you may also spy its populations of musk ox, and Johansen Bay, home to the remains of a nomadic trapper basecamp. Your Expedition Team may also lead you to Ulukhaktok (Holman village) in Queen’s Bay for an unforgettable visit with its local Inuit people. Only in contact with the rest of the world since the middle of the 19th century, the people of Holman continue to practice a traditional lifestyle and represent surprising cultural diversity; experience both with a warm welcome at the shore, drum dances, fresh char and bannock rings (fried dough) prepared over an open fire, and tours of the town art center. Ulukhaktok is famous for its printmaking and you bear witness to the beautiful tradition here; also find carvings, hats made of coveted qiviut (musk ox wool) and ulus — traditional, half-moon-shaped knives used by women to prepare food and skins.   |  <strong style="font-style: italic"> Meals: B L D</strong></p>
