              <h3>What to Bring</h3>
              In December, average daily temperatures will be cold in Canada. Montreal temperatures will range from 22 to 29 degrees, and Quebec a few degrees colder with temperatures ranging between 15 and 22 degrees. You won’t need a suit jacket or tie, as the tour is informal and dinner attire will be casual, but you will want nice outfits for concerts and dinners. Try to pack light and bring only one piece of luggage plus your carry-on. Create mix and match outfits, and think about layering! Pack any medications you require in a carry-on bag and leave expensive jewelry at home.
