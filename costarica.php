              <h3>Money Matters</h3>
              The monetary unit is the colon (CRC), pronounced as “co-lone.” Bills come in denominations of 50, 100, 500, 1000 and 5000 colones. Coins are 50 centimos, and 1, 2, 5, 10, and 20 colones. On December 12, 2016, the interbank exchange rate was 539.2 colones to the US dollar. Exchange money only at banks or at your hotel. It is easier at the hotel, but sometimes the cash drawer is low. Never exchange currency with someone on the street. You will get the best exchange rate by withdrawing money from ATMs, which will be available in all but the smallest villages. Credit cards are widely accepted throughout the country. <a href="https://www.oanda.com/currency/converter/" class="link-select" target="_blank">Click here</a> to see the current exchange rate.<br><br>
              <strong>Tipping in Costa Rica:</strong> A 10 percent service charge is automatically added to restaurant bills, plus another 10 percent tax. Tipping above the automatic charge is at your discretion. Taxi drivers appreciate (but do not expect) tips. Guides and drivers do expect tips. We will offer a group tip to guides and drivers, and more details will be provided before departure.


