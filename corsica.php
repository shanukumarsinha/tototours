              <h4 class="mb-20">About This Tour</h4>
              <p class="mb-15">Close neighbors on the Tyrrhenian Sea, and often rivals throughout the centuries, the sunny islands of Corsica and Sardinia are separated by just 8 miles of water. Their differences, however, are leagues apart. Corsica is known as the “Island of Beauty” for its rugged mountainous interior, while Sardinia is known for its beaches — over 200 of them. Both islands were important waystations for ocean journeys and were coveted by pirates and traders, with each foreign visitor leaving a little something of their heritage behind. The ancient Phoenicians coveted the large rolling landmass of Sardinia, while the Carthaginians coveted the mountainous, fortress-like Corsica. The Roman Empire ruled over both islands for hundreds of years.<br><br>
Corsica is now part of France, and the stone watchtowers that once protected its shores still serve as silent sentinels. Sardinia is now part of Italy, but the prehistoric civilization of the Nuraghic people can still be found in the stone structures left across the island. This comprehensive 2-week tour traverses the entire length of both islands, offering you a taste of Italy and a taste of France.
