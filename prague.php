              <strong>Tipping in Germany:</strong> Unlike some other countries, service staff is always paid by the hour (albeit not always that well). A tip is therefore mainly a matter of politeness and shows your appreciation. Since the introduction of the Euro, a tip (Trinkgeld, lit. “drink money”) of about 5-10% is customary if you were satisfied with the service. Nonetheless, a service charge is already included in an item’s unit price so what you see is what you pay. Tipping in Germany is usually done by mentioning the total while paying. For example, if a waiter tells you the bill amounts to “€13.50”, just state “15” and he will include a tip of €1.50.<br><br>

